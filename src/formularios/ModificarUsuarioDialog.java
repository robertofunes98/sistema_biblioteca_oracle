    private void btnModificarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarUsuarioActionPerformed
        // TODO add your handling code here:
        if(tfClave.getText().isEmpty() && chbModificarClave.isSelected())
            JOptionPane.showMessageDialog(rootPane, "El campo 'Clave' es requerido", "Advertencia", JOptionPane.WARNING_MESSAGE);
        else if(!tfClaveRepetir.getText().equals(tfClave.getText()) && chbModificarClave.isSelected())
            JOptionPane.showMessageDialog(rootPane, "Las claves no coinciden", "Advertencia", JOptionPane.WARNING_MESSAGE);
        else if(cbTipoUsuario.getSelectedIndex()==0)
            JOptionPane.showMessageDialog(rootPane, "Seleccione un tipo de usuario", "Advertencia", JOptionPane.WARNING_MESSAGE);
        else
        {
            try{
                String sql="";
                if(chbModificarClave.isSelected())
                    sql="update oina_usuario set clave = '"+Encriptacion.encrypt(tfClave.getText())+"', tipo_usuario="
                        +(cbTipoUsuario.getSelectedIndex()-1)+" where nombre='"+nombre+"'";
                else
                    sql="update oina_usuario set tipo_usuario="+(cbTipoUsuario.getSelectedIndex()-1)+" where nombre='"+nombre+"'";

                conexion.ejecutarComando(sql);

                JOptionPane.showMessageDialog(this, "Usuario modificado exitosamente", "Aviso", JOptionPane.INFORMATION_MESSAGE);

                this.dispose();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, "error: "+e);
            }
        }
        
    }//GEN-LAST:event_btnModificarUsuarioActionPerformed

    private void chbModificarClaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_chbModificarClaveMouseClicked
        // TODO add your handling code here:
        if(chbModificarClave.isSelected())
        {
            tfClave.setEnabled(true);
            tfClave.setText("");
            tfClaveRepetir.setEnabled(true);
            tfClaveRepetir.setText("");
        }
        else
        {
            tfClave.setEnabled(false);
            tfClave.setText("(Sin modificar)");
            tfClaveRepetir.setEnabled(false);
            tfClaveRepetir.setText("(Sin modificar)");
        }
    }//GEN-LAST:event_chbModificarClaveMouseClicked

